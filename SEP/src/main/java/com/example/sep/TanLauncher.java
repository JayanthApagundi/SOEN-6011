package com.example.sep;

/**
 * Launcher class for the TanCalcGuiD2 application.
 *
 * <p>This class serves as an entry point to launch the GUI-based TAN(x) calculator.
 * It delegates execution to the {@link TanCalcGuiD2} class.
 */
public class TanLauncher {

  /**
   * Main method to launch the GUI-based TAN(x) calculator.
   *
   * @param args command-line arguments (not used)
   */
  public static void main(String[] args) {
    TanCalcGuiD2.main(args);
  }
}
